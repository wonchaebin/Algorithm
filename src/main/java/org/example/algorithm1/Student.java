package org.example.algorithm1;


public class Student {
        private String name;
        private String no;

        public Student(String name, String no) {
            this.name = name;
            this.no = no;
        }

            public String getName() {
                return name;
            }

            public String getNo() {
                return no;
            }
            public void setName(String name) {
             this.name = name;
            }

            public void setNo(String no) {
             this.no = no;
            }

        }
